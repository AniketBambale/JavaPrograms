package ControlStatement;

public class ForLoopP {

	public static void main(String args[]) 
	{
		
		
	/* left side right side angle
	 	*
		**
		***
		****
		*****
		****** */
		
		
		
	 int i,j,row=7;
	 
	 for(i=0;i<row;i++) 
	 {
	System.out.print(""); 
	 
	 for(j=0;j<=i;j++)
	 {
		 System.out.print("*");
	 }
	
	 System.out.println();
	}

	}
}

