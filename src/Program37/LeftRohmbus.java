package Program37;

public class LeftRohmbus {

	public static void main(String[] args) {
     int row=5;
     
     for(int i=1;i<=row;i++)
     {
    	 for(int j=5;j>i;j--)
    		 
    	 {
    		 System.out.print(" ");
    	 }
    	 for(int k=1;k<=row;k++)
    	 {
    		 
    		 System.out.print("*");
    	 }
    	 System.out.println();
    	
     }
     
		
	}

}
